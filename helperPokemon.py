def EvolvingPokemonTwoForm(firstForm, level, secondForm, evolveLevel, evolveChance):
    if(level >= evolveLevel and helperBasic.RollFull(1, 100, 0) <= evolveChance):
        return [[secondForm, level, isShiny()]]
    else:
        return [[firstForm, level, isShiny()]]
    
def EvolvingPokemonThreeForm(firstForm, level, secondForm, evolveLevel1, evolveChance1, thirdForm, evolveLevel2, evolveChance2):
    if(level >= evolveLevel1 and helperBasic.RollFull(1, 100, 0) <= evolveChance1):
        if(level >= evolveLevel2 and helperBasic.RollFull(1, 100, 0) <= evolveChance2):
            return [[thirdForm, level, isShiny()]]
        else:
            return [[secondForm, level, isShiny()]]
    else:
        return [[firstForm, level, isShiny()]]


