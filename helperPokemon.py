def PokemonGenerator(List, level, times):
    totalweight = 0
    encounter = []
    for x in range(0, times):
        encounter.append((helperBasic.RandomWeightedList(List), helperBasic.RollFull(level[0],level[1],level[2]), isShiny()))
            
